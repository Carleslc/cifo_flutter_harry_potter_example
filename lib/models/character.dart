class Character {
  // Person information
  final String name;

  // Attributes
  final int strength;
  final int magicPower;
  final int speed;

  // Image
  final String imageUrl;

  const Character({
    required this.name,
    required this.strength,
    required this.magicPower,
    required this.speed,
    required this.imageUrl,
  });
}
